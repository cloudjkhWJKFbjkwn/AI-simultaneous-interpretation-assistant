/** 按长度降序排列的词典键，用于贪婪最长匹配 */
const SORTED_KEYS = Object.keys(DICTIONARY).sort((a, b) => b.length - a.length);

function normalize(text: string): string {
  return text
    .toLowerCase()
    .replace(/[,.!?;:'"()\[\]{}]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function translateWord(w: string): string {
  return DICTIONARY[w.toLowerCase()] || `[未翻译: ${w}]`;
}

export class MockTranslationService implements TranslationService {
  async translate(text: string): Promise<string> {
    if (!text || text.trim().length === 0) return "";

    const normalized = normalize(text);
    if (!normalized) return text;

    // 先尝试完整短语匹配
    if (DICTIONARY[normalized]) {
      return DICTIONARY[normalized];
    }

    // 贪婪最长子串匹配（由长到短替换）
    let result = normalized;
    const used = new Set<number>();

    for (const key of SORTED_KEYS) {
      let idx = result.indexOf(key);
      while (idx !== -1) {
        // 检查是否被之前的匹配覆盖
        let overlaps = false;
        for (let j = idx; j < idx + key.length; j++) {
          if (used.has(j)) {
            overlaps = true;
            break;
          }
        }
        if (!overlaps) {
          for (let j = idx; j < idx + key.length; j++) used.add(j);
          result =
            result.slice(0, idx) +
            DICTIONARY[key] +
            result.slice(idx + key.length);
        }
        idx = result.indexOf(key, idx + 1);
      }
    }

    // 逐词翻译剩余未匹配部分
    const words = result.split(/\s+/);
    const translated = words.map((w) => {
      // 跳过已有中文
      if (/[\u4e00-\u9fff]/.test(w)) return w;
      return translateWord(w);
    });

    return translated.join("");
  }
}
